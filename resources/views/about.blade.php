            <p>
                As of now main goal is to have a functional e-learning platform where you can register as a student or teacher, and once approved either join or create a course.
                Compared to other such platforms, idea is to make user navigation more straightforwards and add some more modern features like dark mode to website.
            </p>
            <p>Teachers will given set of these features:</p>
            <ul class="list-disc list-inside">
                <li>Add a new subject to course list</li>
                <li>Edit new subject and provide all necessary details such as description and entry requirements.</li>
                <li>Ability to approve or reject people applying for your course</li>
                <li>Add modules to course and set weekly schedule for each module</li>
                <li>Manage learning resources including lectures and assessments per module basis</li>
                <li>Grading and feedback system to manage pupils progress during course</li>
                <li>Interactive dashboard that displays insights about students and their progress</li>
                <li>Possibly direct one to one chat option, and any additional features that might be useful</li>
            </ul>
            <p>As for students the following set of features will be offered:</p>
            <ul class="list-disc list-inside">
                <li>Ability to make new account and be able to use it once registration gets approved</li>
                <li>Personalization such as ability to add and change account information</li>
                <li>Apply for any course, and once approved be able to start viewing resources related to it</li>
                <li>Interactive dashboard to track personal progress through course</li>
                <li>Dedicated sections for resources and assignments to avoid confusion and make user experience easier</li>
